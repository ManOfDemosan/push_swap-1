#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char		*temp;
	size_t		i;

	i = 0;
	temp = (char *)malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (!temp)
		return (NULL);
	while (s1[i] != '\0')
	{
		temp[i] = s1[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
